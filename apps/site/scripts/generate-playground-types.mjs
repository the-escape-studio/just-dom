/**
 * Bakes packages/just-dom/dist/index.d.ts into a string for Monaco extraLib.
 * Run after `just-dom` is built. Invoked from `apps/site` via `pnpm run generate:playground-types`.
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const justDomDts = join(
  __dirname,
  "..",
  "..",
  "..",
  "packages",
  "just-dom",
  "dist",
  "index.d.ts",
);
const outFile = join(
  __dirname,
  "..",
  "src",
  "views",
  "playground",
  "playground-globals.generated.ts",
);

const raw = readFileSync(justDomDts, "utf8");
const withoutExport = raw.replace(/\nexport \{[^]*\};\s*$/, "\n");
const forMonaco = `${withoutExport}\ndeclare const mount: HTMLDivElement;\n`;
const asTs = `// AUTO-GENERATED by apps/site/scripts/generate-playground-types.mjs -- do not edit by hand
// Re-run: pnpm run generate:playground-types (from apps/site) or pnpm run build (site)
export const PLAYGROUND_TYPES = ${JSON.stringify(forMonaco)} as const;
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, asTs, "utf8");
console.log("Wrote", outFile);
